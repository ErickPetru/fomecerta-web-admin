<script>
import { getMessage } from '@/helpers/messages'
import restrictGuests from '@/mixins/restrict-guests'

export default {
  name: 'PageTOS',
  middleware: 'auth-tos',
  mixins: [restrictGuests],
  data () {
    return {
      loading: true
    }
  },
  mounted () {
    this.loading = false
  },
  methods: {
    async logout () {
      try {
        await this.$fireAuth.signOut()
      } catch (error) {
        console.error(error)
        this.$snackbar.showMessage(getMessage(error))
      }
    },

    async accept () {
      try {
        this.loading = true

        const timestamp = this.$fireStoreObj.Timestamp.now()
        await this.$fireStore.doc(`establishments/${this.authUser.uid}`).set(
          {
            termsAccepted: timestamp
          },
          {
            merge: true
          }
        )

        const authUser = { ...this.authUser }
        authUser.termsAccepted = true
        this.$store.commit('SET_AUTH_USER', { authUser })

        this.$router.push('/')
      } catch (error) {
        console.error(error)
        this.$snackbar.showMessage(getMessage(error), 'error')
      } finally {
        this.loading = false
      }
    }
  },
  head: () => ({
    title: 'Termos e condições gerais'
  })
